#include <unistd.h>

void	ft_print_reverse_alphabet(void)
{
	char	a[26];
	int		i;

	i = 0;
	while (i < 26)
	{
		a[i] = 'z' - i;
		i++;
	}
	write(1, &a, 26);
}
